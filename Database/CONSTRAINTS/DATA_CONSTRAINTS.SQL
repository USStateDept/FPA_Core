BEGIN
	RAISE INFO 'CREATE of DATA FOREIGN CONSTRAINTS';

	ALTER TABLE FIND.DATA
		OWNER TO finddbuser;

	ALTER TABLE FIND.DATA ADD CONSTRAINT COUNTRY_ID_FK1
		FOREIGN KEY (COUNTRY_ID)
		REFERENCES FIND.COUNTRY(COUNTRY_ID) MATCH SIMPLE
		ON UPDATE NO ACTION ON DELETE NO ACTION;
	ALTER TABLE FIND.DATA CONSTRAINT INDICATOR_ID_FK1
	 	FOREIGN KEY (INDICATOR_ID)
		REFERENCES FIND.INDICATOR(INDICATOR_ID) MATCH SIMPLE
		ON UPDATE NO ACTION ON DELETE NO ACTION;

	CREATE INDEX COUNTRY_ID_IDX ON FIND.DATA USING BTREE (COUNTRY_ID);
	CREATE INDEX INDICATOR_ID_IDX ON FIND.DATA USING BTREE (INDICATOR_ID);
	CREATE INDEX YEAR_IDX ON FIND.DATA USING BTREE (YEAR);
END $$;
